package chapter3

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestKadai1(t *testing.T) {
	k := Kadai1{
		id:   1,
		name: "hoge",
	}

	assert.Equal(t, 1, k.ID())
	assert.Equal(t, "hoge", k.Name())

	k.SetID(2)
	k.SetName("fuga")

	assert.Equal(t, 2, k.ID())
	assert.Equal(t, "fuga", k.Name())
}
